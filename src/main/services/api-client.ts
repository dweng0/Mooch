export interface SubscriptionStatus {
  isActive: boolean
  subscriptionStatus: string | null
  subscriptionPlan: string | null
  availableProviders: AIProvider[]
  transcriptionsUsed: number
  transcriptionLimit: number | null
  snapshotsUsed: number
  snapshotLimit: number | null
  hasCodeSnapshot: boolean
}

export async function fetchSubscriptionStatus(): Promise<ApiResponse<SubscriptionStatus>> {
  const apiKeys = loadApiKeys()
  // Build query string with API keys if they exist
  const params = new URLSearchParams()
  if (apiKeys.anthropicApiKey) params.set('anthropicApiKey', apiKeys.anthropicApiKey)
  if (apiKeys.geminiApiKey) params.set('geminiApiKey', apiKeys.geminiApiKey)
  if (apiKeys.openaiApiKey) params.set('openaiApiKey', apiKeys.openaiApiKey)
  
  const queryString = params.toString()
  const path = queryString 
    ? `/api/copilot/subscription-status?${queryString}` 
    : '/api/copilot/subscription-status'
  
  return request<SubscriptionStatus>('GET', path)
}

export async function transcribeAudio(audioBuffer: ArrayBuffer): Promise<string> {
  const base64 = Buffer.from(audioBuffer).toString('base64')
  const apiKeys = loadApiKeys()
  const result = await request<{ transcript: string }>('POST', '/api/copilot/transcribe', { 
    audio: base64,
    apiKeys,
  })
  if (!result.ok) {
    if (result.status === 401) clearSession()
    throw new Error(result.message)
  }
  return result.data.transcript
}


/**
 * Login with email + password via Wasp's email auth endpoint.
 * On success the session cookie is persisted to disk (encrypted).
 */
export async function loginWithEmail(email: string, password: string): Promise<void> {
  // Wasp email/password login endpoint (generated by wasp for email auth method)
  const res = await fetch(`${WASP_API_URL}/auth/email/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email, password }),
  })

  if (!res.ok) {
    const body = await res.json().catch(() => ({}))
    throw new Error((body as { message?: string }).message ?? 'Invalid email or password')
  }

  // Wasp returns the sessionId in the response body
  const body = await res.json().catch(() => ({})) as { sessionId?: string }
  const sessionId = body.sessionId

  if (!sessionId) {
    throw new Error('Login succeeded but no session ID was returned')
  }

  // Store the sessionId (it will be sent as Bearer token in Authorization header)
  saveSession(sessionId)
}

